--
-- drug
--

CREATE TABLE drug (
  id VARCHAR(45) NOT NULL,
  num VARCHAR(45) NOT NULL,
  drug_serl VARCHAR(45) NOT NULL,
  drug_type VARCHAR(45) NOT NULL,
  gen_name VARCHAR(1024) DEFAULT NULL,
  bland_name VARCHAR(45) DEFAULT NULL,
  route VARCHAR(1024) DEFAULT NULL,
  start_date VARCHAR(1024) DEFAULT NULL,
  end_date VARCHAR(1024) DEFAULT NULL,
  amount VARCHAR(1024) DEFAULT NULL,
  tani VARCHAR(1024) DEFAULT NULL,
  times VARCHAR(1024) DEFAULT NULL,
  object VARCHAR(1024) DEFAULT NULL,
  response VARCHAR(45) DEFAULT NULL,
  react VARCHAR(1024) DEFAULT NULL,
  bunrui VARCHAR(45) DEFAULT NULL,
  PRIMARY KEY (id, drug_serl),
  KEY idx_drid (id)
) ENGINE=InnoDB DEFAULT CHARSET=sjis;

