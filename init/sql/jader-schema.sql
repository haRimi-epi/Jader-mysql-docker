  id VARCHAR(45) NOT NULL,
  num VARCHAR(45) NOT NULL,
  sex VARCHAR(45) NOT NULL,
  age VARCHAR(45) NOT NULL,
  weight VARCHAR(45) DEFAULT NULL,
  hight VARCHAR(45) DEFAULT NULL,
  time VARCHAR(45) NOT NULL,
